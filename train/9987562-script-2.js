function SoundOne() {
  var myAudio = document.getElementById("PadOne");
  if (myAudio.paused) {
    myAudio.play();
  } else {
    myAudio.pause();
  }
  document.onkeydown = function(event) {
  if (event.keyCode == 49) {
    myAudio.play();
  }
}
}

